    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        jLabel6.setText("");
        String login = jTextField1.getText();
        String pass = jPasswordField1.getText();
        String query = "SELECT * FROM user";
        user1 u = new user1();
        try
        {
            con = DriverManager.getConnection(url, user, password);
            stmt = con.createStatement();
            rs = stmt.executeQuery(query); 
            while (rs.next()) 
            {
                if(rs.getString(2).equals(login))
                {
                    u.setId(rs.getInt(1));
                    u.setLogin(rs.getString(2));
                    u.setPass(rs.getString(3));
                    u.setName(rs.getString(4));
                }
            }
        } 
        catch (SQLException sqlEx) 
        {
                sqlEx.printStackTrace();
        } 
        finally 
        {
                //close connection ,stmt and resultset here
                try { con.close(); } catch(SQLException se) { /*can't do anything */ }
                try { stmt.close(); } catch(SQLException se) { /*can't do anything */ }
                try { rs.close(); } catch(SQLException se) { /*can't do anything */ }
        }
        if(u.getLogin() == null)
        {
            jLabel6.setText("Користувача з таким логіном немає");
        }
        else
        {
            if(!pass.equals(u.getPass()))
            {
                jLabel6.setText(jLabel6.getText()+"   Невірний пароль");
            }
            else
            {
                MainMenu form12 = new MainMenu(u);
                form12.setTitle("Головне меню");
                this.hide();
                form12.show();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

